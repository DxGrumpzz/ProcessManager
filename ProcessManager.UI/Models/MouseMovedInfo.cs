namespace ProcessManager.UI
{
    /// <summary>
    /// A simple class that contains info about a generic mouse moved event
    /// </summary>
    public class MouseMovedInfo
    {
        /// <summary>
        /// A boolean flag that indicates if the left mouse button was pressed at the time of the event
        /// </summary>
        public bool LeftButtonPressed;

        /// <summary>
        /// A boolean flag that indicates if the right mouse button was pressed at the time of the event
        /// </summary>
        public bool RightButtonPressed;
    };

};